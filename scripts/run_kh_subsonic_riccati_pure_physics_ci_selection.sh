  --riccati-boundary-band-points "${RICCATI_BOUNDARY_BAND_POINTS:-32}" \
  --riccati-boundary-band-start "${RICCATI_BOUNDARY_BAND_START:-0.94}" \
  --riccati-boundary-band-end "${RICCATI_BOUNDARY_BAND_END:-0.995}" \
