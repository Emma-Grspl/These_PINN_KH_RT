  --n-boundary "${N_BOUNDARY:-64}" \
  --n-alpha-supervision "${N_ALPHA_SUPERVISION:-128}" \
  --n-anchor-alpha "${N_ANCHOR_ALPHA:-16}" \
  --n-norm-interior "${N_NORM_INTERIOR:-256}" \
  --n-reference-alpha "${N_REFERENCE_ALPHA:-81}" \
